let puntaje = 5;

// Incrementos: posincremento
console.log(puntaje++); // 5
console.log(puntaje);   // 6

// Incremento: preincremento
console.log(++puntaje); // 7
console.log(puntaje);   // 7

// Decremento: posdecremento
console.log(puntaje--); // 7
console.log(puntaje);   // 6

// Decremento: predecremento
console.log(--puntaje); // 5
console.log(puntaje);   // 5

// Incrementar en 3 el valor actual de una variable y asignarselo
puntaje += 3;
console.log(puntaje);

// Decrementar en 3 el valor actual de una variable y asignarselo
puntaje -= 3;
console.log(puntaje);